    private void btnsaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsaveActionPerformed
        int r = jTable1.getSelectedRow();
        if (r > -1) {
            list.remove(r);
            if (txtbienso.getText().length() != 0 && txttenchuxe.getText().length() != 0 && txtmauxe.getText().length() != 0 && txtngaygui.getText().length() != 0) {

                quanlyxe s = new quanlyxe();

                s.setBienso(txtbienso.getText());
                s.setTenchuxe(txttenchuxe.getText());
                s.setMauxe(txtmauxe.getText());
                s.setLoaixe(loaixe.getSelectedItem().toString());
                s.setNgaygui(txtngaygui.getText());

                if (r < list.size()) {
                    list.add(r, s);
                } else {
                    list.add(s);
                }

                file.ghixe(list);
                H1();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Hãy nhập đầy đủ thông tin");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Chọn 1 hàng để sửa rồi nhấn lưu!");
        }

// TODO add your handling code here:
    }//GEN-LAST:event_btnsaveActionPerformed


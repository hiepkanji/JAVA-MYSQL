    private void btnsapxepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsapxepActionPerformed
        // TODO add your handling code here:
        int r = boxsapxep.getSelectedIndex();
        if(r ==0){
            Collections.sort(list, new Comparator<quanlyxe>() {
                @Override
                public int compare(quanlyxe t, quanlyxe t1) {
                    return t.getTenchuxe().substring(t.getTenchuxe().lastIndexOf(" "))
                    .compareTo(t1.getTenchuxe().substring(t1.getTenchuxe().lastIndexOf(" ")));
                }
            });
        }

        if(r ==1){
            Collections.sort(list, new Comparator<quanlyxe>() {
                @Override
                public int compare(quanlyxe t, quanlyxe t1) {
                    return t.getLoaixe().substring(t.getLoaixe().lastIndexOf(" "))
                    .compareTo(t1.getLoaixe().substring(t1.getLoaixe().lastIndexOf(" ")));
                }
            });
        }

        model.setRowCount(0);
        for(quanlyxe b: list)
        model.addRow(b.toObject());
        
    }//GEN-LAST:event_btnsapxepActionPerformed

